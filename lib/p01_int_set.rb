class MaxIntSet
  def initialize(max = 8)
    @store = Array.new(max, false)
  end

  def insert(num)
    validate!(num)
    return false if @store[num] #if store is already true, returns false
    @store[num] = true
  end

  def remove(num)
    return nil unless include?(num) #returns nil if@store[num] is not true
    @store[num] = false
    num

  end

  def include?(num)
    @store[num]
  end

  private

  def is_valid?(num)
    num.between?(0, @store.length - 1)
  end

  def validate!(num)
    raise "Out of bounds" unless is_valid?(num)
  end
end
